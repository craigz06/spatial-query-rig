def run_ana(video_path, seam=None):
    """
    Invoke ANA v1.48 on the given video file. ANA writes its outputs into the
    video's folder.

    If `seam` indicates stereo AND this ANA build accepts seam/stereo overrides,
    DCI passes --seam-x <col> --force-stereo so the spatial signal runs even
    when aspect ratio < 2.5 (the standard-resolution SBS case). Otherwise DCI
    falls back to the legacy call and prints a note.
    """
    import subprocess
    import sys

    if not os.path.exists(ANA_SCRIPT_PATH):
        print(f"[ANA SKIP] ANA script not found at: {ANA_SCRIPT_PATH}")
        print(f"           Edit ANA_SCRIPT_PATH at the top of this file to point at your ana_v148.py")
        return False

    cmd = [sys.executable, ANA_SCRIPT_PATH, os.path.basename(video_path)]

    if seam and seam.get("is_stereo"):
        caps = ana_capabilities()
        if caps["seam_x"] and caps["force_stereo"]:
            cmd += ["--seam-x", str(seam["seam_x"]), "--force-stereo"]
            print(f"  Stereo SBS confirmed (seam x={seam['seam_x']}). "
                  f"Passing override to ANA -> spatial signal ENABLED.")
        else:
            print("  [ANA NOTE] Stereo SBS detected by DCI, but this ANA build")
            print("             does not accept --seam-x / --force-stereo.")
            print("             ANA will fall back to its aspect-ratio gate and")
            print("             will classify this 1920x1080 SBS as MONOCULAR,")
            print("             so the spatial (H-disparity) signal is lost.")
            print(f"             Detected seam x={seam['seam_x']} "
                  f"(offset {seam.get('center_offset_px', 0):+d}px).")
            print("             Patch ana_v148.py to accept these two flags to")
            print("             light up spatial on standard-resolution SBS.")

    try:
        print(f"  Running ANA v1.48 on {os.path.basename(video_path)}...")
        video_dir = os.path.dirname(os.path.abspath(video_path)) or "."
        result = subprocess.run(
            cmd,
            cwd=video_dir,
            capture_output=True,
            text=True,
            timeout=ANA_TIMEOUT_S
        )
        if result.stdout:
            for line in result.stdout.splitlines():
                if any(k in line for k in ("Questioner:", "Proximity:",
                        "Spacetime:", "CoV=", "spread=", "t=")):
                    print(f"   {line.strip()}")
        if result.returncode == 0:
            return True
        else:
            print(f"[ANA ERROR] Exit code {result.returncode}")
            if result.stderr:
                print(f"  stderr: {result.stderr[-500:]}")
            return False
    except subprocess.TimeoutExpired:
        print(f"[ANA ERROR] Timed out after {ANA_TIMEOUT_S} seconds.")
        return False
    except Exception as e:
        print(f"[ANA ERROR] {e}")
        return False


