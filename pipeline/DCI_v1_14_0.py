def _read_text(path):
    """Read a text file if it exists; return '' on any failure."""
    try:
        with open(path, "r", encoding="utf-8", errors="replace") as f:
            return f.read()
    except Exception:
        return ""


def write_sco_stub(video_basename):
    """
    Write {stem}_sco_report.json holding the SCO INPUTS only:
        capture metadata + transcript (srt) + ANA json + ANA report.
    sco_findings is null — Claude fills it during the online frame-by-frame
    pass and writes the completed report to Google Drive ("old stereo reports").
    Returns the stub path, or None. Never raises.
    """
    if not SCO_STUB_ENABLED:
        return None

    stem = os.path.splitext(video_basename)[0]
    srt        = f"{stem}.srt"
    ana_json   = f"{stem}_ana_v1.48.json"
    ana_report = f"{stem}_ana_v1.48_report.md"

    stub = {
        "capture": video_basename,
        "timestamp": time.strftime("%Y-%m-%dT%H:%M:%S"),
        "schema": "sco_report.v1",
        "pass_status": "stub",   # -> "complete" once Claude fills findings
        "inputs": {
            "transcript_srt": _read_text(srt) or None,
            "ana_json": _read_text(ana_json) or None,
            "ana_report_md": _read_text(ana_report) or None,
        },
        "sco_findings": None,    # filled online: snap / surprise / account + QI
        "drive_folder": "old stereo reports",
    }

    out_path = f"{stem}_sco_report.json"
    try:
        import json
        with open(out_path, "w", encoding="utf-8") as f:
            json.dump(stub, f, indent=2, ensure_ascii=False)
        print(f">> SCO stub saved: {out_path}  (findings filled online)")
        return out_path
    except Exception as e:
        print(f"[SCO STUB WARN] Could not write {out_path}: {e}")
        return None


def cleanup_diagnostics(folder="."):
    """
    Remove ANA diagnostic artifacts not part of the SCO bundle:
        *_anaglyph.mp4, *_spacetime.png, *_orient.png
    Keeps .mp4 / .wav / .srt / _ana_v1.48.json / _ana_v1.48_report.md / _sco_report.json.
    Never raises.
    """
    import glob
    removed = []
    for pattern in SCO_CLEANUP_GLOBS:
        for path in glob.glob(os.path.join(folder, pattern)):
            try:
                os.remove(path)
                removed.append(os.path.basename(path))
            except Exception as e:
                print(f"[CLEANUP WARN] Could not remove {path}: {e}")
    if removed:
        print(f">> Cleaned diagnostics: {', '.join(removed)}")
    return removed


