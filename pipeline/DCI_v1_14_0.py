def _seam_from_gray(gray, verbose=True):
    """
    Core seam locator. Operates on a single averaged grayscale image (H x W).
    Returns the seam dict. Off-center seams are preserved; we only fall back to
    frame center when NO dark central band is found at all.
    """
    H, W = gray.shape[:2]
    if W < 8:
        return {"is_stereo": False, "seam_x": W // 2, "center_offset_px": 0,
                "confidence": "NONE", "width": W, "height": H,
                "reason": "frame_too_small"}

    col_mean = gray.mean(axis=0)
    col_var  = gray.var(axis=0)
    global_mean = float(col_mean.mean())

    c0 = max(1, int(W * (0.5 - SEAM_SEARCH_FRAC)))
    c1 = min(W - 1, int(W * (0.5 + SEAM_SEARCH_FRAC)))

    dark_thr = SEAM_DARK_FRAC * global_mean
    var_thr  = float(np.percentile(col_var[c0:c1], 40))
    cand = [x for x in range(c0, c1)
            if col_mean[x] < dark_thr and col_var[x] <= var_thr]

    if not cand:
        # No dark divider found -> monocular. Do NOT invent a seam.
        return {"is_stereo": False, "seam_x": W // 2, "center_offset_px": 0,
                "confidence": "NONE", "width": W, "height": H,
                "reason": "no_dark_central_band"}

    # Longest contiguous run of dark columns = the divider band.
    runs, cur = [], [cand[0]]
    for x in cand[1:]:
        if x == cur[-1] + 1:
            cur.append(x)
        else:
            runs.append(cur); cur = [x]
    runs.append(cur)
    best = max(runs, key=len)
    band_lo, band_hi = best[0], best[-1]
    seam_center = (band_lo + band_hi) // 2
    band_width = band_hi - band_lo
    center_offset = seam_center - W // 2

    # Confidence: real divider bands are reasonably wide and near (but not
    # necessarily at) center. "A bit" off-center stays HIGH.
    abs_off = abs(center_offset)
    if band_width < 4 or abs_off > W * SEAM_OFFSET_LOW_FRAC:
        confidence = "LOW"
    elif band_width < 10 or abs_off > W * SEAM_OFFSET_MED_FRAC:
        confidence = "MEDIUM"
    else:
        confidence = "HIGH"

    seam = {
        "is_stereo": True,
        "seam_x": int(seam_center),
        "seam_x0": int(max(0, band_lo - SEAM_PAD_PX)),
        "seam_x1": int(min(W - 1, band_hi + SEAM_PAD_PX)),
        "band_width_px": int(band_width),
        "center_offset_px": int(center_offset),
        "confidence": confidence,
        "width": int(W), "height": int(H),
        "reason": "dark_divider_detected",
    }
    return seam


def detect_stereo_seam(source, verbose=True, probe_frames=SEAM_PROBE_FRAMES):
    """
    Detect a beam-splitter seam (dark vertical divider) and decide stereo SBS.

    Accepts either:
      - a BGR frame (np.ndarray)  -> fast single-frame probe (record-start use)
      - a video path (str)        -> authoritative multi-frame averaged probe

    Returns a seam dict (see _seam_from_gray). Key fields:
        is_stereo        : bool
        seam_x           : int   true divider column (may be off-center)
        center_offset_px : int   seam_x - W//2  (negative = left of center)
        confidence       : HIGH | MEDIUM | LOW | NONE
    """
    # ---- ndarray frame: single-frame probe ----
    if isinstance(source, np.ndarray):
        gray = cv2.cvtColor(source, cv2.COLOR_BGR2GRAY).astype(np.float32)
        seam = _seam_from_gray(gray, verbose=verbose)
        if verbose:
            _print_seam(seam, tag="PROBE/live")
        return seam

    # ---- path: average several frames for a stable read ----
    cap = cv2.VideoCapture(source)
    if not cap.isOpened():
        return {"is_stereo": False, "seam_x": 0, "center_offset_px": 0,
                "confidence": "NONE", "width": 0, "height": 0,
                "reason": "cannot_open_video"}
    total = int(cap.get(cv2.CAP_PROP_FRAME_COUNT)) or 1
    idxs = np.linspace(0, max(0, total - 1),
                       min(total, probe_frames)).astype(int)
    grays = []
    for i in idxs:
        cap.set(cv2.CAP_PROP_POS_FRAMES, int(i))
        ok, f = cap.read()
        if ok:
            grays.append(cv2.cvtColor(f, cv2.COLOR_BGR2GRAY).astype(np.float32))
    cap.release()
    if not grays:
        return {"is_stereo": False, "seam_x": 0, "center_offset_px": 0,
                "confidence": "NONE", "width": 0, "height": 0,
                "reason": "no_frames_read"}
    gray = np.mean(grays, axis=0)
    seam = _seam_from_gray(gray, verbose=verbose)
    if verbose:
        _print_seam(seam, tag="STEREO/file")
    return seam


def _print_seam(seam, tag="STEREO"):
    """Operator-facing one-liner for a seam result."""
    if seam.get("is_stereo"):
        off = seam.get("center_offset_px", 0)
        off_txt = (f"offset {off:+d}px from center"
                   if off != 0 else "centered")
        print(f"[{tag}] SBS seam x={seam['seam_x']} ({off_txt}), "
              f"band {seam.get('band_width_px', '?')}px, "
              f"confidence {seam.get('confidence', '?')}")
    else:
        print(f"[{tag}] no central divider -> MONOCULAR "
              f"({seam.get('reason', 'n/a')})")


