def beep(times=1, freq=880, dur_ms=120, gap_ms=90):
    """
    Short audible cue. Single beep = record start, double = stop & save.
    Real tone via sounddevice; falls back to the terminal bell.
    Never raises — an audio cue must never break the pipeline.
    """
    try:
        if AUDIO_AVAILABLE:
            fs = 44100
            t = np.linspace(0, dur_ms / 1000.0, int(fs * dur_ms / 1000.0), False)
            tone = (0.3 * np.sin(2 * np.pi * freq * t)).astype(np.float32)
            for i in range(times):
                sd.play(tone, fs, blocking=True)
                if i < times - 1:
                    time.sleep(gap_ms / 1000.0)
            return
    except Exception:
        pass
    for i in range(times):
        print("\a", end="", flush=True)
        if i < times - 1:
            time.sleep(gap_ms / 1000.0)


