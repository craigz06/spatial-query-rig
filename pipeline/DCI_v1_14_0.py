def run_camera_pipeline():
    os.system('clear')

    frame_width = 1920
    frame_height = 1080
    encoder_fps = 30.0
    cache_video_filename = "sco_working_cache.mp4"
    cache_audio_filename = "sco_working_cache.wav"
    window_name = "Grounded Observer - Live Stream Monitor"

    print("=================================================================")
    print("DCI v1.14.0 — Direct Camera Interface")
    print("            record + transcribe + ANA v1.48 + stereo seam + SCO stub")
    print("=================================================================")
    if not AUDIO_AVAILABLE:
        print("[WARNING] Run: pip3 install sounddevice")
    if not WHISPER_AVAILABLE:
        print("[INFO] Whisper not installed. Auto-transcription disabled.")
        print("       pip3 install openai-whisper")
    ana_present = os.path.exists(ANA_SCRIPT_PATH)
    if not ana_present:
        print(f"[INFO] ANA v1.48 not found at: {ANA_SCRIPT_PATH}")
        print(f"       Edit ANA_SCRIPT_PATH at top of script to enable physics analysis.")
    else:
        print(f"[OK] ANA v1.48 detected at: {ANA_SCRIPT_PATH}")
        caps = ana_capabilities()
        if caps["seam_x"] and caps["force_stereo"]:
            print("[OK] ANA accepts --seam-x / --force-stereo "
                  "(spatial signal will run on standard-res SBS).")
        else:
            print("[WARN] ANA does NOT accept --seam-x / --force-stereo. "
                  "Standard-res SBS will be analyzed temporal-only until ANA is patched.")
    print("[OK] Native stereo seam detection active (ana_stereo.py not required).")
    audio_device, audio_label = select_audio_device()
    if AUDIO_AVAILABLE:
        print(f"[AUDIO] Input device: {audio_label}")
    print(f"Scanning for camera (indices {CAMERA_SCAN_RANGE if CAMERA_INDEX_OVERRIDE is None else [CAMERA_INDEX_OVERRIDE]})...")

    cap, active_index = find_active_sensor()
    if cap is None:
        print("[CRITICAL] No camera found. Check USB and macOS Privacy > Camera.")
        return

    cap.set(cv2.CAP_PROP_FRAME_WIDTH, frame_width)
    cap.set(cv2.CAP_PROP_FRAME_HEIGHT, frame_height)
    actual_width = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    actual_height = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))

    video_writer = None
    audio_thread = None
    audio_stop_event = threading.Event()
    audio_shared_state = {'paused': False}
    is_recording = False
    is_paused = False
    frame_count = 0
    live_stereo_tag = "stereo: probing..."   # updated at record start

    print(f"Sensor locked: Index {active_index} at {actual_width}x{actual_height}")
    cv2.namedWindow(window_name, cv2.WINDOW_NORMAL)

    try:
        while True:
            ret, frame = cap.read()
            if not ret:
                print("[ERROR] Frame drop.")
                break

            frame_count += 1
            timestamp = time.strftime("%Y-%m-%d %H:%M:%S")

            if is_recording and not is_paused and video_writer is not None:
                video_writer.write(frame)

            display_frame = frame.copy()

            if not is_recording:
                status_text = "IDLE"
                status_color = (150, 150, 150)
            elif is_paused:
                status_text = "PAUSED"
                status_color = (0, 255, 255)
            else:
                indicator = " *" if (frame_count // 15) % 2 == 0 else ""
                status_text = f"RECORDING{indicator}"
                status_color = (0, 0, 255)

            draw_unified_dashboard(display_frame, status_text, status_color,
                                   timestamp, is_recording, "DUAL_TRACK_CACHE",
                                   active_index, live_stereo_tag)
            cv2.imshow(window_name, display_frame)
            key = cv2.waitKey(1) & 0xFF

            if key == ord('r') or key == ord('R'):
                if not is_recording:
                    fourcc = cv2.VideoWriter_fourcc(*'avc1')
                    video_writer = cv2.VideoWriter(cache_video_filename, fourcc, encoder_fps, (actual_width, actual_height))
                    if not video_writer.isOpened():
                        fourcc = cv2.VideoWriter_fourcc(*'mp4v')
                        video_writer = cv2.VideoWriter(cache_video_filename, fourcc, encoder_fps, (actual_width, actual_height))
                    if AUDIO_AVAILABLE:
                        audio_stop_event.clear()
                        audio_shared_state['paused'] = False
                        audio_thread = threading.Thread(
                            target=audio_recorder_worker,
                            args=(cache_audio_filename, audio_stop_event,
                                  audio_shared_state, audio_device))
                        audio_thread.start()
                    is_recording = True
                    is_paused = False
                    print(f"[{timestamp}] Recording started.")
                    beep(times=1)  # single beep = record start

                    # ---- FRONT-LOADED STEREO PROBE (live, advisory) ----
                    try:
                        live_seam = detect_stereo_seam(frame, verbose=True)
                        if live_seam.get("is_stereo"):
                            off = live_seam.get("center_offset_px", 0)
                            live_stereo_tag = (f"stereo: SBS x={live_seam['seam_x']} "
                                               f"({off:+d}px, {live_seam['confidence']})")
                        else:
                            live_stereo_tag = "stereo: MONOCULAR"
                    except Exception as e:
                        live_stereo_tag = "stereo: probe error"
                        print(f"[STEREO PROBE] error: {e}")
                elif is_paused:
                    is_paused = False
                    audio_shared_state['paused'] = False
                    print(f"[{timestamp}] Resumed.")

            elif key == ord('p') or key == ord('P'):
                if is_recording and not is_paused:
                    is_paused = True
                    audio_shared_state['paused'] = True
                    print(f"[{timestamp}] Paused.")

            elif key == ord('s') or key == ord('S'):
                if is_recording:
                    beep(times=2)  # double beep = stop & save
                    if video_writer:
                        video_writer.release()
                        video_writer = None
                    if AUDIO_AVAILABLE and audio_thread:
                        audio_stop_event.set()
                        audio_thread.join()
                        audio_thread = None
                    cv2.destroyAllWindows()
                    for _ in range(15):
                        cv2.waitKey(1)
                    os.system('clear')
                    print("=" * 50)
                    user_given_name = input("Filename (ENTER for default): ").strip() or "sco_sequence"
                    user_given_name = user_given_name.replace(" ", "_")
                    time_suffix = time.strftime("%Y%m%d_%H%M%S")
                    final_video_name = f"{user_given_name}_{time_suffix}.mp4"

                    if os.path.exists(cache_video_filename):
                        final_audio_name = f"{user_given_name}_{time_suffix}.wav"
                        final_srt_name = f"{user_given_name}_{time_suffix}.srt"

                        if AUDIO_AVAILABLE and os.path.exists(cache_audio_filename):
                            # Always preserve the WAV — signature analysis needs it
                            import shutil
                            shutil.copy(cache_audio_filename, final_audio_name)
                            print(f">> Audio saved: {final_audio_name}")

                            # Mux into MP4 (best effort - WAV stays regardless)
                            print("Merging audio + video (imageio-ffmpeg)...")
                            if merge_audio_video(cache_video_filename, cache_audio_filename, final_video_name):
                                print(f">> Video+audio merged: {final_video_name}")
                            else:
                                os.rename(cache_video_filename, final_video_name)
                                print(f">> Merge failed. Video saved silent: {final_video_name}")

                            # Auto-transcribe with whisper
                            if WHISPER_AVAILABLE:
                                print("Transcribing...")
                                if transcribe_to_srt(final_audio_name, final_srt_name):
                                    print(f">> Transcript saved: {final_srt_name}")
                                else:
                                    print(">> Transcription failed.")
                            else:
                                print(">> Skipping transcript (whisper not installed)")

                            # Clean up cache files
                            for f in [cache_video_filename, cache_audio_filename]:
                                try: os.remove(f)
                                except: pass
                        else:
                            os.rename(cache_video_filename, final_video_name)
                            print(f">> Video only: {final_video_name}")

                        # ---- AUTHORITATIVE STEREO DETECTION on saved MP4 ----
                        print()
                        print("Stereo seam detection (authoritative, multi-frame)...")
                        seam = detect_stereo_seam(final_video_name, verbose=True)

                        # ---- Auto-run ANA v1.48 (stereo-aware) ----
                        if os.path.exists(ANA_SCRIPT_PATH):
                            print()
                            print("Running ANA v1.48 physics analysis...")
                            if run_ana(final_video_name, seam=seam):
                                print(">> ANA v1.48 bundle saved (json + md)")
                            else:
                                print(">> ANA failed — check console output above")
                        else:
                            print(">> Skipping ANA (script not found)")

                        # ---- Write SCO stub (inputs only; findings filled online) ----
                        write_sco_stub(final_video_name)

                        # ---- Remove ANA diagnostic artifacts (keep the bundle) ----
                        cleanup_diagnostics(folder=".")

                        print()
                        print("=" * 60)
                        print(f"COMPLETE BUNDLE: {user_given_name}_{time_suffix}")
                        print("=" * 60)
                        print(f"  DCI media : .mp4 / .wav / .srt")
                        if seam.get("is_stereo"):
                            print(f"  STEREO SBS: seam x={seam['seam_x']} "
                                  f"(offset {seam.get('center_offset_px', 0):+d}px, "
                                  f"{seam.get('confidence', '?')} confidence)")
                        else:
                            print(f"  MONOCULAR : no central divider detected")
                        if os.path.exists(ANA_SCRIPT_PATH):
                            print(f"  ANA report: _ana_v1.48.json / _ana_v1.48_report.md")
                        print(f"  SCO stub  : _sco_report.json (findings filled online)")
                        print()
                        print("Upload the bundle to Claude online for frame-by-frame SCO.")
                        print("Claude fills the findings and saves the report to Drive")
                        print("(\"old stereo reports\").")

                    is_recording = False
                    is_paused = False
                    live_stereo_tag = "stereo: probing..."
                    print("Resuming live monitor...")
                    cv2.namedWindow(window_name, cv2.WINDOW_NORMAL)

            elif key == ord(' '):
                snap_time = time.strftime("%Y%m%d_%H%M%S")
                snap_filename = f"sco_frame_{snap_time}.png"
                cv2.imwrite(snap_filename, frame, [int(cv2.IMWRITE_PNG_COMPRESSION), 0])
                print(f"Snapshot: {snap_filename}")

            elif key == ord('q') or key == ord('Q'):
                break

    except KeyboardInterrupt:
        print("\nInterrupted.")
    finally:
        if video_writer: video_writer.release()
        if AUDIO_AVAILABLE and audio_thread:
            audio_stop_event.set()
            audio_thread.join()
        for f in [cache_video_filename, cache_audio_filename]:
            if os.path.exists(f):
                try: os.remove(f)
                except: pass
        cap.release()
        cv2.destroyAllWindows()
        for _ in range(5):
            cv2.waitKey(1)
        print("Pipeline closed.")


if __name__ == "__main__":
    run_camera_pipeline()
