_ANA_CAPS_CACHE = None

def ana_capabilities():
    """
    Run `ana_v148.py --help` once and cache which override flags it supports.
    Returns dict: {"seam_x": bool, "force_stereo": bool}
    A stock ANA v1.48 supports neither (it gates spatial on aspect ratio).
    """
    global _ANA_CAPS_CACHE
    if _ANA_CAPS_CACHE is not None:
        return _ANA_CAPS_CACHE
    caps = {"seam_x": False, "force_stereo": False}
    if not os.path.exists(ANA_SCRIPT_PATH):
        _ANA_CAPS_CACHE = caps
        return caps
    try:
        import subprocess, sys
        out = subprocess.run([sys.executable, ANA_SCRIPT_PATH, "--help"],
                             capture_output=True, text=True, timeout=30)
        helptext = (out.stdout or "") + (out.stderr or "")
        caps["seam_x"] = "--seam-x" in helptext
        caps["force_stereo"] = ("--force-stereo" in helptext
                                or "--stereo" in helptext)
    except Exception as e:
        print(f"[ANA] capability probe failed ({e}); assuming legacy CLI.")
    _ANA_CAPS_CACHE = caps
    return caps


